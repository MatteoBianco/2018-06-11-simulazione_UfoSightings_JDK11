package it.polito.tdp.ufo.model;

public class CountryPair {

	private String c1;
	private String c2;
	
	public CountryPair(String c1, String c2) {
		super();
		this.c1 = c1;
		this.c2 = c2;
	}

	public String getC1() {
		return c1;
	}

	public String getC2() {
		return c2;
	}
	
}
